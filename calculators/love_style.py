def calculate(person_a: dict, person_b: dict) -> dict:
    """リーの恋愛スタイル6類型の相性を計算"""
    mbti_a = (person_a.get('mbti') or '').upper().replace("-A", "").replace("-T", "").strip()
    mbti_b = (person_b.get('mbti') or '').upper().replace("-A", "").replace("-T", "").strip()
    if not mbti_a or not mbti_b:
        return None

    style1_a, style2_a = MBTI_TO_STYLE.get(mbti_a, ("ストルゲ", "エロス"))
    style1_b, style2_b = MBTI_TO_STYLE.get(mbti_b, ("ストルゲ", "エロス"))

    key = (style1_a, style1_b)
    rev_key = (style1_b, style1_a)
    score, score_100, summary = STYLE_COMPAT.get(key) or STYLE_COMPAT.get(rev_key, (3, 70, ""))

    name_a = person_a.get('name', '男性')
    name_b = person_b.get('name', '女性')

    return {
        "name": "リーの恋愛スタイル6類型",
        "category": "love_style",
        "icon": "local_fire_department",
        "score": score,
        "score_100": score_100,
        "summary": f"{style1_a} × {style1_b} — {summary}",
        "details": {
            "person_a": {"primary": style1_a, "secondary": style2_a},
            "person_b": {"primary": style1_b, "secondary": style2_b},
        },
        "highlights": [
            f"{name_a}: {style1_a}（{STYLES[style1_a]}）/ 副: {style2_a}",
            f"{name_b}: {style1_b}（{STYLES[style1_b]}）/ 副: {style2_b}",
        ],
        "advice": summary,
    }
